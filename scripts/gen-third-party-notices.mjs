// Generates THIRD_PARTY_NOTICES.md from npm (license-checker JSON) and Rust
// (cargo metadata JSON) dependency data. Regenerate from the repo root with:
//   npx license-checker --production --json > .tpn-npm.json
//   (cd src-tauri && cargo metadata --format-version 1 > ../.tpn-cargo.json)
//   node scripts/gen-third-party-notices.mjs
//   rm .tpn-npm.json .tpn-cargo.json
import { readFileSync, writeFileSync } from 'node:fs';

const npm = JSON.parse(readFileSync('.tpn-npm.json', 'utf8'));
const cargo = JSON.parse(readFileSync('.tpn-cargo.json', 'utf8'));

// --- npm: drop our own root package, sort by name ---
const npmRows = Object.entries(npm)
  .filter(([id]) => !id.startsWith('parchmint@'))
  .map(([id, v]) => {
    const at = id.lastIndexOf('@');
    return { name: id.slice(0, at), version: id.slice(at + 1), license: v.licenses };
  })
  .sort((a, b) => a.name.localeCompare(b.name));

// --- cargo: drop our own crate, sort by name ---
const ours = new Set(['parchmint']);
const seen = new Set();
const cargoRows = cargo.packages
  .filter((p) => !ours.has(p.name))
  .map((p) => ({ name: p.name, version: p.version, license: p.license || p.license_file || 'see source' }))
  .filter((p) => (seen.has(p.name + p.version) ? false : seen.add(p.name + p.version)))
  .sort((a, b) => a.name.localeCompare(b.name));

const table = (rows) =>
  ['| Package | Version | License |', '|---|---|---|',
   ...rows.map((r) => `| ${r.name} | ${r.version} | ${r.license} |`)].join('\n');

const out = `# Third-Party Notices

Parchmint bundles third-party open-source software. This file lists those dependencies and
their licenses. All are permissive (MIT / ISC / BSD / Apache-2.0 / Zlib / Unicode / CC0 /
MPL-2.0 file-level), compatible with Parchmint's MIT license. Full license texts ship with
each package (under \`node_modules/\` and in each crate's source).

This file is generated by \`scripts/gen-third-party-notices.mjs\`.

## JavaScript / npm dependencies (${npmRows.length})

${table(npmRows)}

## Rust / Cargo dependencies (${cargoRows.length})

${table(cargoRows)}
`;

writeFileSync('THIRD_PARTY_NOTICES.md', out);
console.log(`wrote THIRD_PARTY_NOTICES.md — ${npmRows.length} npm, ${cargoRows.length} cargo`);
